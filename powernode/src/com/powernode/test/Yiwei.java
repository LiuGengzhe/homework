package com.powernode.test;

public class Yiwei {
    public static void main(String[] args) {
        int i = 7;
        int j = 1;
        int x = -3;
        System.out.println(i>>1);
        System.out.println(j<<2);
        System.out.println(x<<1);
        System.out.println(-5>>1);
        System.out.println(-5<<1);
        System.out.println(~3);
    }
}
